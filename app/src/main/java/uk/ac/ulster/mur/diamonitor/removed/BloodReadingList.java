public class BloodReadingList {


    private String BloodID;
    private String BloodReading;
    private String BloodTime;
    private String insulinId;
    private String insulinUnits;
    private String insulinTime;



    public String getBloodID() {
        return BloodID;
    }

    public void setBloodID(String bloodID) {
        BloodID = bloodID;
    }

    public String getBloodReading() {
        return BloodReading;
    }

    public void setBloodReading(String bloodReading) {
        BloodReading = bloodReading;
    }


    public String getBloodTime() {
        return BloodTime;
    }

    public void setBloodTime(String bloodTime) {
        BloodTime = bloodTime;
    }



    public String getInsulinId() {
        return insulinId;
    }

    public void setInsulinId(String insulinId) {
        this.insulinId = insulinId;
    }


    public String getInsulinUnits() {
        return insulinUnits;
    }

    public void setInsulinUnits(String insulinUnits) {
        this.insulinUnits = insulinUnits;
    }


    public String getInsulinTime() {
        return insulinTime;
    }

    public void setInsulinTime(String insulinTime) {
        this.insulinTime = insulinTime;
    }


}
